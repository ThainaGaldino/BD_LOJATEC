 CREATE TABLE PRODUTO (
 IDPRODUTO INTEGER PRIMARY KEY NOT NULL ,
 NOMEPRDOUTO VARCHAR (30)NOT NULL,
 PRECOUNITARIO NUMERIC (12,2) NOT NULL ,
 IDFORNECEDOR INTEGER NOT NULL
 )
 GO

 CREATE TABLE ITEMNOTA (
 NUMERONOTA INTEGER NOT NULL ,
 IDPRODUTO INTEGER NOT NULL,
 QTDELITEM INTEGER NOT NULL,
 VALORITEM NUMERIC (10,2) NOT NULL 
 )
 GO

 CREATE TABLE CLIENTE (
 IDCLIENTE INTEGER NOT NULL ,
 NOMECLIENTE VARCHAR (20)NOT NULL ,
